package com.xlntsmmr.xlnt_timeline.DTO;

public class ConfigUpdateNewsDTO {

    private String latest_ver;
    private String update_news;
    private String new_function;

    // Getter 및 Setter 메서드 (필요한 경우)

    public String getLatest_ver() {
        return latest_ver;
    }

    public void setLatest_ver(String latest_ver) {
        this.latest_ver = latest_ver;
    }

    public String getUpdate_news() {
        return update_news;
    }

    public void setUpdate_news(String update_news) {
        this.update_news = update_news;
    }

    public String getNew_function() {
        return new_function;
    }

    public void setNew_function(String new_function) {
        this.new_function = new_function;
    }
}
